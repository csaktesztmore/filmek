class Film:
    def __init__(self, title, title_url, image, rate, year, certificate, time, genre, iplratingstar, content, director_url, director, star1_url, star1, star2_url, star2, star3_url, star3, star4_url, star4, votes, gross = "unkown"):
        self.title = title
        self.title_url = title_url
        self.image = image
        self.rate = rate
        self.year = year
        self.certificate = certificate
        self.time = time
        self.genre = genre
        self.iplratingstar = iplratingstar
        self.content = content
        self.director_url = director_url
        self.director = director
        self.star1_url = star1_url
        self.star1 = star1
        self.star2_url = star2_url
        self.star2 = star2
        self.star3_url = star3_url
        self.star3 = star3
        self.star4_url = star4_url
        self.star4 = star4
        self.votes = votes
        self.gross = gross
    def __str__(self) -> str:
        return f"{self.genre}"
        